package command.garage;

/**
 * @author 谢迪
 * Date 2023年09月22日 16:14:00
 * Description 车库门
 * @Version: v1.0
 */
public class GarageDoor {

    /**
     * 打开车库门
     */
    public void on() {
        System.out.println("打开车库门");
    }

    /**
     * 关闭车库门
     */
    public void off() {
        System.out.println("关闭车库门");
    }

    /**
     * 停止关闭车库门
     */
    public void stop() {
        System.out.println("停止关闭车库门");
    }

    /**
     * 打开车库灯
     */
    public void lightOn() {
        System.out.println("停止关闭车库门");
    }

    /**
     * 关闭车库灯
     */
    public void lightOff() {
        System.out.println("停止关闭车库门");
    }


}
